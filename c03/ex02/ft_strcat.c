char	*ft_strcat(char *dest, char *src)
{
	int	d;
	int	i;

	d = 0;
	while (dest[d] != '\0')
		d++;
	i = 0;
	while (src[i] != '\0')
	{
		dest[d] = src[i];
		d++;
		i++;
	}
	dest[d] = '\0';
	return (dest);
}
